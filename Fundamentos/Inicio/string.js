const nome = "Galdino"

console.log(nome.charAt(4)) // i
console.log(nome.charAt(5)) // n
console.log(nome.charCodeAt(3)) // 100
console.log(nome.indexOf('l')) // 2
console.log(nome.substring(1)) // aldino
console.log(nome.substring(0, 3)) // Gal
console.log('Nome: '.concat(nome).concat('!')) // Nome: Galdino!
console.log(nome.replace('d', 't')) // Galtino
